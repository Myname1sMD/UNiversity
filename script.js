window.filterUniversities = function() {
    const city = document.getElementById('city-filter').value;
    const subject1 = document.getElementById('subject1').value;
    const subject2 = document.getElementById('subject2').value;

    const filtered = universityData.filter(uni => {
        // –§–∏–ª—å—Ç—Ä –ø–æ –≥–æ—Ä–æ–¥—É
        const cityMatch = !city || uni.city === city;

        // –§–∏–ª—å—Ç—Ä –ø–æ –ø—Ä–µ–¥–º–µ—Ç–∞–º (–≥–∏–±–∫–∞—è –ª–æ–≥–∏–∫–∞: —Ö–æ—Ç—è –±—ã –æ–¥–∏–Ω –∏–∑ –≤—ã–±—Ä–∞–Ω–Ω—ã—Ö –ø—Ä–µ–¥–º–µ—Ç–æ–≤ –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –ø—Ä–æ—Ñ–∏–ª—å–Ω—ã–º)
        const subjectMatch = uni.sections.programs.list.some(program => {
            const required = program.subjects_required;
            const sub1Match = !subject1 || required.includes(subject1);
            const sub2Match = !subject2 || required.includes(subject2);
            
            // –ü—Ä–æ–≥—Ä–∞–º–º–∞ –ø–æ–¥—Ö–æ–¥–∏—Ç, –µ—Å–ª–∏ –æ–Ω–∞ —Ç—Ä–µ–±—É–µ—Ç –ª–∏–±–æ sub1, –ª–∏–±–æ sub2 (–∏–ª–∏ –æ–±–∞, –µ—Å–ª–∏ –≤—ã–±—Ä–∞–Ω—ã)
            // –ò—Å–∫–ª—é—á–µ–Ω–∏–µ: –µ—Å–ª–∏ –≤—ã–±—Ä–∞–Ω—ã –æ–±–∞, –æ–Ω–∏ –¥–æ–ª–∂–Ω—ã –±—ã—Ç—å –≤ —Ç—Ä–µ–±–æ–≤–∞–Ω–∏—è—Ö –ø—Ä–æ–≥—Ä–∞–º–º—ã.
            if (subject1 && subject2) {
                return required.includes(subject1) && required.includes(subject2);
            } else {
                return sub1Match && sub2Match; // –ï—Å–ª–∏ –≤—ã–±—Ä–∞–Ω —Ç–æ–ª—å–∫–æ –æ–¥–∏–Ω, —Ç–æ –æ–Ω –¥–æ–ª–∂–µ–Ω —Å–æ–≤–ø–∞—Å—Ç—å
            }
        });
        
        return cityMatch && subjectMatch;
    });

    renderUniversities(filtered);
};

